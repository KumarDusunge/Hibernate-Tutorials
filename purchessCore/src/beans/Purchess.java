package beans;

public class Purchess {
	private int pid;
	private String purchessItem;
	private String date;
	private double amount;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getPurchessItem() {
		return purchessItem;
	}
	public void setPurchessItem(String purchessItem) {
		this.purchessItem = purchessItem;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}

}
